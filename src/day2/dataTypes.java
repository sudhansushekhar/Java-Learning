package day2;

public class dataTypes {

	//There are two types of data types- Primitive and Non-Primitive
	// Pritimive -> Integer(byte-1, short-2, int-4, long-8), Decimal/float (float-4, double-8), Boolean(true/false-1), textual/character(char-2)
	//Non-Primitive -> classes, String, arrays & interfaces
	
	
	public static void main(String[] args) {
		//Primitive data types
		int a = 10;
		double b= 2.7;
		char c='A';//char-single quotes but String - double quotes
		boolean bool = true;
		
		//Non-Primitive data types
		String name = "Sudhanshu"; //String in double quotes
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(bool);
		System.out.println(name);
		
	}
}
