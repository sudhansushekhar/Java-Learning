package day10_Constructors;

public class ConstructorWithoutMainMethod {
	//attributes of class
	public int year; 
	public String car;
	
	public ConstructorWithoutMainMethod(String carName, int yr)	// this is constructor with parameter
	{
		//initialize objects
		car= carName; // initialize values to attribute
		year=yr; 
	}
	


}
